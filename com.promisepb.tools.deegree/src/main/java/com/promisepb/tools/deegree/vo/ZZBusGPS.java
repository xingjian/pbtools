public class ZZBusGPS {

	private String id;
	private String time;
	private String x;
	private String y;
	private String speed;
	private String mzl;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getMzl() {
		return mzl;
	}
	public void setMzl(String mzl) {
		this.mzl = mzl;
	}
	
}
